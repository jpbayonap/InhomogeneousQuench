    // IntegratorFn using Gauss-Legendre of chosen order (64 or 128)
    inline double integrate_gauss(const std::function<double(double)> &f,
                                  double a, double b,
                                  int order = 128)
    {
        std::vector<double> x, w;
        if (order >= 128)
        {
            gauss_legendre_128(x, w);
            order = 128;
        }
        else
        {
            gauss_legendre_64(x, w);
            order = 64;
        }

        const double c1 = 0.5 * (b - a);
        const double c2 = 0.5 * (b + a);

        double sum = 0.0;
        for (int i = 0; i < order; ++i)
        {
            const double t = c1 * x[i] + c2;
            sum += w[i] * f(t);
        }
        return c1 * sum;
    }

    // Split the interval into 2^min_rec segments (clamped by max_splits if >0) and integrate each
    inline double integrate_gauss_split(const std::function<double(double)> &f,
                                        double a, double b,
                                        int order = 128,
                                        int min_rec = 0,
                                        std::size_t max_splits = 0)
    {
        std::size_t splits = 1ull << std::max(0, min_rec);
        if (max_splits > 0)
        {
            splits = std::min<std::size_t>(splits, max_splits);
        }
        if (splits == 0)
            splits = 1;

        const double h = (b - a) / static_cast<double>(splits);
        double total = 0.0;
        for (std::size_t i = 0; i < splits; ++i)
        {
            double aa = a + h * static_cast<double>(i);
            double bb = aa + h;
            total += integrate_gauss(f, aa, bb, order);
        }
        return total;
    }

    // Convenience IntegratorFn wrapper (default 128-pt, with optional forced splits)
    inline IntegratorFn make_gauss_integrator(int order = 128,
                                              int min_rec = 0,
                                              std::size_t max_splits = 0)
    {
        return [=](const std::function<double(double)> &f,
                   double a, double b)
        {
            return integrate_gauss_split(f, a, b, order, min_rec, max_splits);
        };
    }

